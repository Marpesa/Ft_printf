#include "ft_printf.h"

int	ft_flag_zero(const char *s, int i, int l)
{
	int	n;
	int	r;

	r = 0;
	n = 0;
	i++;
	while (s[i] >= '0' && s[i] <= '9')
	{
		n += (s[i] - 48);
		if (s[i + 1] >= '0' && s[i + 1] <= '9')
			n *= 10;
		i++;
	}
	r = n;
	if (n > l)
	{
		while (n - l)
		{
			write (1, "0", 1);
			n--;
		}
	}
	return (r - n);
}
