#include "ft_printf.h"

int	ft_flag_int(const char *s, int i, int l)
{
	int		n;
	int		t;
	char	c;

	c = ' ';
	n = 0;
	if (s[i] == '0')
		c = '0';
	while (s[i] >= '0' && s[i] <= '9')
	{
		n += s[i++] - 48;
		if (s[i] >= '0' && s[i] <= '9')
			n *= 10;
	}
	t = n;
	if (s[i] == 's' || s[i] == 'c')
		c = ' ';
	if (n > l)
	{
		while (n-- > l)
			write (1, &c, 1);
		return (t - l);
	}
	return (0);
}
