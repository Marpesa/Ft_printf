#include "ft_printf.h"

int	ft_flag_int(const char *s, int i, int l)
{
		int	n;
		int	t;

		n = 0;
		while (s[i] >= '0' && s[i] <= '9')
		{
			n += s[i] - 48;
			i++;
			if (s[i] >= '0' && s[i] <= '9')
				n *= 10;
		}
		t = n;
		if (n > l)
		{
			while ((n - l) > 0)
			{
				write (1, " ", 1);
				n--;
			}
		return (t - l);
		}
		return (0);
}
